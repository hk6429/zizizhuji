function optionParts(option, anchors) {
  const idiom = [...anchors]
    .filter((candidate) => option.includes(candidate))
    .sort((a, b) => option.lastIndexOf(b) - option.lastIndexOf(a) || b.length - a.length)[0];
  if (!idiom) throw new Error(`選項找不到錨定成語：${option}`);
  const evidence = option
    .replace(/^(?:老師看到|校刊記下|家人聽說|社團談到)/u, '')
    .replace(/，(?:不禁感嘆|編輯直呼|忍不住說|社員紛紛感嘆)：.*$/u, '')
    .replace(/，真是[^，。！？]+[。！？]?$/u, '')
    .trim();
  return { idiom, evidence };
}


function cleanEvidence(raw) {
  let text = raw
    .replace(/，真是.*$/u, '')
    .replace(/，讓大家印象深刻。?$/u, '')
    .replace(/^用以|^用來|^可拿來|^借來說明|^說的是|^泛指|^意指|^是說|^借指|^指/u, '')
    .replace(/^形容|^比喻|^描寫|^呈現/u, '')
    .replace(/；(?:也可|呈現|描寫|泛指|比擬).*/u, '')
    .replace(/，(?:也就是|後泛指|泛指|比喻|形容|意指|多用於|現在常|指|借指|描寫|呈現|比擬).*/u, '')
    .replace(/；/g, '，')
    .replace(/某件事情|某件事|這件事情/g, '這次企畫')
    .replace(/某個人|一個人/g, '阿哲')
    .replace(/別人/g, '隊友')
    .replace(/事物/g, '展品')
    .replace(/東西/g, '物品')
    .replace(/[：；]$/u, '')
    .trim();
  return text;
}

function sceneFor(raw, seed) {
  const sceneOpenings = ['', '這回，', '當天，', '事後，'];
  const decorate = (scene) => `${sceneOpenings[seed % sceneOpenings.length]}${scene}`;
  if (exactScenes.has(raw)) return decorate(exactScenes.get(raw));
  const cleaned = cleanEvidence(raw);
  for (const [pattern, scene] of semanticScenes) {
    if (pattern.test(cleaned)) return decorate(scene);
  }
  const settings = ['決賽那天', '社區會議後', '校慶當晚', '週末市集裡', '成果展結束前', '班會討論時', '採訪現場', '返鄉途中'];
  let clause = cleaned
    .replace(/^人/u, '阿哲')
    .replace(/^小安/u, '小晴')
    .replace(/自身的?/g, '自己的')
    .replace(/同學/g, '同伴')
    .replace(/某件創舉/g, '這項創舉')
    .replace(/這件事/g, '這次事件')
    .replace(/身處/g, '待在')
    .replace(/污濁/g, '混亂')
    .replace(/能夠/g, '可以')
    .replace(/不能/g, '沒辦法')
    .replace(/沒有任何/g, '毫無')
    .replace(/沒有/g, '沒')
    .replace(/極大的?/g, '嚴重的')
    .replace(/小小的/g, '一點')
    .replace(/十分/g, '很')
    .replace(/非常/g, '很')
    .replace(/正是/g, '恰好是')
    .replace(/終於實現/g, '總算成真')
    .replace(/盼望已久/g, '等了多年')
    .replace(/年紀雖老/g, '即使年過七十')
    .replace(/雄心壯志/g, '遠大志向')
    .replace(/一起做壞事/g, '聯手作弊')
    .replace(/小毛病、小過失/g, '報告裡的一個錯字')
    .replace(/奪取.*?的地位而取代他/u, '搶走組長的位置取而代之')
    .replace(/處理小這次事件/u, '只為修好一張椅子')
    .replace(/長遠而艱鉅的工作/u, '得花上多年心力')
    .replace(/用同一個道理或原則貫穿處理所有/u, '始終依同一原則處理每項')
    .replace(/國家內部/g, '國內')
    .replace(/外部又遭受侵擾/g, '邊境又遭敵軍侵犯');
  if (clause.length > 27) {
    const parts = clause.split(/[，；]/u).filter(Boolean);
    const joined = parts.slice(0, 2).join('，');
    clause = joined.length <= 27 ? joined : parts[0];
    if (clause.length > 27) clause = clause.slice(0, 27).replace(/[，、：；]$/u, '');
  }
  const verbPhrase = /^(?:先|待|只|厚報|即使|不|可以|有|倚仗|選擇|把|用|將|從|面對|遇到|主動|竭盡|看見|聽到|得到|失去|做|說|走|穿|喝|拿|抱|登|專心|專注|沉迷|忍|拒|依|順|跟|故意|提醒|集合|開創|記取|立|抬|刮|送|奉|建立|更換|揮|拔|接近|靠近|對|與|為|因|已|仍|奪|投|逃|躲|相信|認為|喜歡|討厭)/u.test(clause);
  return `${settings[seed % settings.length]}，${verbPhrase ? `阿哲${clause}` : clause}`;
}

